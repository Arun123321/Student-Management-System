     * Creating new Subject
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $subject=new Subject;
        $subject->name=$request->name;

        
        if ($subject->save()) {
           
            $subjects=Subject::all();

            return Redirect::back()->with([
                'data' =>$subjects,
            ]);

        } else {
            return Redirect::route('dashboard');
        }
    }

